/**
 * Traduz o nome de uma seleção de PT-BR para EN
 * Retorna o nome original se não houver tradução (para países cujos nomes são iguais)
 * @param {string} ptName
 * @returns {string}
 */
export function translateTeamName(ptName) {
  if (!ptName) return '';
  const key = ptName.toLowerCase().trim();
  return TEAM_NAME_MAP[key] || ptName;
}
